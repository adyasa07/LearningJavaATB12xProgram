package ex_02_JavaBasics2;

public class Lab010_Variables {
    public static void main(String[] args) {

        // datatype variableName = variableValue

        byte age=65;
        // byte --> dataType
        //age-->variableName/Identifier
        //= ---> assignment operator
        //65--->variableValue

        age=65+1;
        System.out.println(age);

        //we can change the variable value anytime
        //it will take the latest value assigned
    }



}
