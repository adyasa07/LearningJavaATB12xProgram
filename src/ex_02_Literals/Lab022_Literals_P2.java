package ex_02_Literals;

public class Lab022_Literals_P2 {

    public static void main(String[] args) {
        int age=65;// Decimal System---> base 10

        System.out.println(age);

        //Binary Literals
        int age_bin=0b1000001;
        System.out.println(age_bin);

        //Octal Literal
        int age_oct=0101;
        System.out.println(age_oct);

        //Hexadecimal Literal
        int age_hex=0x41;
        System.out.println(age_hex);

    }


}
