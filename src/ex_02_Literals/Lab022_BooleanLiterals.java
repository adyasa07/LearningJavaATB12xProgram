package ex_02_Literals;

public class Lab022_BooleanLiterals {

    public static void main(String[] args) {
        boolean isJavaFun=true;
        boolean isCheeseCakeTasty= true;
        boolean isFishTasty=false;

        System.out.printf("Is Java Fun ?\n Ans: %b\n",isJavaFun);
        System.out.printf("Is Cheesecake Tasty ?\n Ans: %b\n",isCheeseCakeTasty);
        System.out.printf("Is Fish Tasty ?\n Ans: %b\n",isFishTasty);
    }

}
